export async function getTableDescriptions(htmlContent: string) {
  const promptTemplate: string = `User will provide a page extracted from the PDF and converted to HTML.
Please describe the HTML tables and generate a concise title that captures the essence of the table and a description of the information in the table.
Return the result as a JSON containing tableId (id of the table), tableTitle (the generated title), and tableDescription (the generated description).
Here is how the output should look like:
[
{
  "tableId": "",
  "tableTitle": "",
  "tableDescription": ""
}
]
If there are no HTML tables then just return an empty JSON list.
The table title and table description should be in Romanian.`;

  const headers = {
    Authorization: "Bearer " + MISTRAL_API_KEY,
    "Content-Type": "application/json",
  };

  const body = {
    model: "open-mistral-nemo",
    max_tokens: 8192,
    temperature: 0,
    messages: [
      {
        role: "system",
        content: promptTemplate,
      },
      {
        role: "user",
        content: htmlContent,
      },
    ],
  };

  const response = await axios.post(
    `${MISRAL_API_ENDPOINT}/v1/chat/completions`,
    body,
    {
      headers,
    }
  );

  const jsonResult = response.data;

  const llmAnswer: string = jsonResult.choices[0].message.content;

  // ensure JSON response
  if (llmAnswer.indexOf("[") >= 0) {
    const llmAnswerJson = llmAnswer.slice(
      llmAnswer.indexOf("["),
      llmAnswer.indexOf("]") + 1
    );
    return JSON.parse(llmAnswerJson);
  }
  return [];
}

export async function getEmbeddings(content: string) {
  const myHeaders = new Headers();
  myHeaders.append("Authorization", "Bearer " + DEEPINFRA_API_KEY);
  myHeaders.append("Content-Type", "application/json");

  const raw = JSON.stringify({
    model: "BAAI/bge-m3",
    input: content,
    encoding_format: "float",
  });

  const requestOptions: any = {
    method: "POST",
    headers: myHeaders,
    body: raw,
    redirect: "follow",
  };

  const response = await fetch(
    `${DEEPINFRA_API_ENDPOINT}/v1/openai/embeddings`,
    requestOptions
  );
  const llmAnswer = await response.json();
  return llmAnswer.data[0].embedding;
}

export function euclideanDistance(vector1: number[], vector2: number[]) {
  let sum = 0;
  for (let i = 0; i < vector1.length; i++) {
    sum += Math.pow(vector1[i] - vector2[i], 2);
  }

  return Math.sqrt(sum);
}

